public class Toan_Tu_So_Hoc_5 {
    public static void main(String[] args) {
        // Cac toan tu so hoc
        int a = 10;
        int b = 20;
        System.out.println("Ket qua phep cong: " + (a+b));
        System.out.println("Ket qua phep tru: " + (a-b));
        System.out.println("Ket qua chia lay phan nguyen: " + (a/b));
        System.out.println("Ket qua chia lay du: " + (a%b));
        // Cac toan tu gan
        System.out.println("a+=20 tuong duong a = a + 20, cac phep toan con lai tuong tu");
        // Toan tu so sanh
        System.out.println("Cac toan tu so sanh se tra ve kieu boolean voi ket qua True va Flase");
        System.out.println("a == b: "+(a == b));
        System.out.println("a > b: "+(a > b));
        System.out.println("a != b: "+(a != b));
    }
}
